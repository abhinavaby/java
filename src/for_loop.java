public class for_loop {
    public static void main(String[] args) {
        for(int j=1;j<11;j++){
            System.out.println(j);
        }
        System.out.println(" ");
        for(int j=1;j<11;j+=2){
            System.out.println(j);
        }

        System.out.println(" ");
        for(int j=10;j>1;j--){
            System.out.println(j);
        }
    }
}

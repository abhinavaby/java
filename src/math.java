public class math {
    public static void main(String[] args) {
        System.out.println(Math.PI);
        System.out.println(Math.E);
        System.out.println(Math.cos(Math.PI));


        double pow = Math.pow(2,3);
        System.out.println(pow);


        System.out.println("absolute value of -5 is "+Math.abs(-5));
        System.out.println("square root of 9 "+Math.sqrt(9));
        System.out.println("round of 3.14 is "+Math.round(3.14));
        System.out.println("round of 3.14 is "+Math.ceil(3.14));
        System.out.println("round of 3.14 is "+Math.floor(3.14));


        System.out.println(Math.max(45,43));
        System.out.println(Math.min(45,43));
    }
}
